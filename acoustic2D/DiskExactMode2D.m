% Exact modal disk solution
function [vx, vy, p, beta, R0] = DiskExactMode2D(t, x, y, root)

beta = 7;

R0 = [ 8.577836489714073, ...
      12.932386237089576, ...
      16.529365884366943, ...
      19.941853366527340, ...
      23.268052926457571, ...
      26.545032061823576, ...
      29.790748583196613, ...
      33.015178641375144, ...
      36.224380548787160, ...
      39.422274578939259, ...
      42.611522172286683, ...
      45.793999658055000, ...
      48.971070951900593, ...
      52.143752969301985, ...
      55.312820330403447, ...
      58.478874029898776, ...
      61.642387847310793, ...
      64.803740533649247, ...
      67.963238640648242, ...
      71.121133035731432, ...
      74.277631059986035, ...
      77.432905619556067, ...
      80.587102080112899, ...
      83.740343562142897, ...
      86.892735055240692, ...
      90.044366648713606, ...
      93.195316092975645, ...
      96.345650848540146, ...
      99.495429738673508];

R0 = R0(root);

A = [1.801155757842105e+01, ...
     4.246960098781376e+01, ...
     6.386601291288043e+01, ...
     8.610355632372162e+01, ...
     1.095512139856252e+02, ...
     1.342743867588460e+02, ...
     1.602651850764896e+02, ...
     1.874933213834177e+02, ...
     2.159218357315301e+02, ...
     2.455127828511661e+02, ...
     2.762294190472164e+02, ...
     3.080370142690826e+02, ...
     3.409030858579206e+02, ...
     3.747973825697352e+02, ...
     4.096917634878370e+02, ...
     4.455600380256906e+02, ...
     4.823777977699543e+02, ...
     5.201222542003486e+02, ...
     5.587720882335724e+02, ...
     5.983073135843982e+02, ...
     6.387091540265519e+02, ...
     6.799599337550779e+02, ...
     7.220429796937478e+02, ...
     7.649425344963612e+02, ...
     8.086436790247113e+02, ...
     8.531322631782899e+02, ...
     8.983948440646712e+02, ...
     9.444186306162194e+02, ...
     9.911914338694711e+02];

A = A(root);

r = hypot(x, y);
q = atan2(y, x);

r_x = x ./ r;
r_y = y ./ r;
q_x = - y ./ r.^2;
q_y =   x ./ r.^2;

r_x(~isfinite(r_x)) = 0;
r_y(~isfinite(r_y)) = 0;
q_x(~isfinite(q_x)) = 0;
q_y(~isfinite(q_y)) = 0;

R   = besselj(beta, R0 * r);
R_r = (R0 / 2) .* (besselj(beta-1, R0 * r) - besselj(beta+1, R0 * r));

alpha = R0;
T    =         -alpha^2 * cos(alpha * t - beta .* q);
IT   =          alpha   * sin(alpha * t - beta .* q);
IT_q = -beta .* alpha   * cos(alpha * t - beta .* q);

p = R .* T;
vx = r_x .* R_r .* IT + q_x .* R .* IT_q;
vy = r_y .* R_r .* IT + q_y .* R .* IT_q;

p = p / A;
vx = vx / A;
vy = vy / A;
